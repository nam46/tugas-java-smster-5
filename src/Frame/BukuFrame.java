    private void produkItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_produkItemStateChanged
        // TODO add your handling code here:
        Connection connection = null;
        Statement statement = null;
        String result = null;

        String showData = "SELECT * FROM kategori WHERE nama_kategori='" + produk.getSelectedItem() + "'";
        try {
            connection = Koneksi.getConnection();
            Statement stm = connection.createStatement();
            ResultSet res = stm.executeQuery(showData);
            while (res.next()) {
                fieldIdKat.setText(res.getString(5));
            }

        } catch (Exception ex) {
                Logger.getLogger(BukuFrame.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_produkItemStateChanged

