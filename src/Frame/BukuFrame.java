    private void tableBukuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableBukuMouseClicked
        int lineTable = tableBuku.rowAtPoint(evt.getPoint());

        String idBuku = tableBuku.getValueAt(lineTable, 0).toString();
        fieldIdBuk.setText(idBuku);
        
        String idKat = tableBuku.getValueAt(lineTable, 0).toString();
        fieldIdKat.setText(idKat);

        String judul = tableBuku.getValueAt(lineTable, 2).toString();
        fieldJudul.setText(judul);

        String stok = tableBuku.getValueAt(lineTable, 3).toString();
        fieldStok.setText(stok);

        String harga = tableBuku.getValueAt(lineTable, 4).toString();
        fieldHarga.setText(harga);
        
        String pengarang = tableBuku.getValueAt(lineTable, 5).toString();
        fieldPengarang.setText(pengarang);
        
        buttonInsertUser.setEnabled(false);
        buttonDeleteUser.setEnabled(true);
        buttonUpdateUser.setEnabled(true);
    }//GEN-LAST:event_tableBukuMouseClicked

